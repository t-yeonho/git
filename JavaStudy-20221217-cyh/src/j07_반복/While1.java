package j07_반복;

public class While1 {

	public static void main(String[] args) {
		System.out.println("프로그램 시작");
		int i = 0; //초기문
		
		while(i < 5) { //조건문
			System.out.println(i); //실행문
			i++; //후처리문
		}
		System.out.println("프로그램 종료");
		
		/*
		 * <while 문의 기본구조>
		 * while(조건문) {
		 * 		<수행할문장1>;
		 * 		<수행할문장2>;
		 * 		<수행할문장3>;
		 * 		...
		 * }
		 */
		
		
		
	}

}
