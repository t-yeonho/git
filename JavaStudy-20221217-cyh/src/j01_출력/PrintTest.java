package j01_출력;

public class PrintTest {

	public static void main(String[] args) {
		System.out.println("Hello, Java!");
		
		System.out.print("최연호");
		System.out.println("-----");
		
		System.out.print("주말수업");
		System.out.println("--");
		
		// 주석 : 프로그램에 영향을 주지 않는 글
//		실행 단축키 : ctrl + F11
//		주석 단축키 : ctrl + /
//		println 은 줄 바꿈해라임.
		
		System.out.print("Hello, \nJava\n");
		System.out.println("최연호");
//		\n : 줄바꿈
		
		int level;
		level = 10;
		System.out.println(level);
		level = 11;
		System.out.println(level);
		
	}

}




















