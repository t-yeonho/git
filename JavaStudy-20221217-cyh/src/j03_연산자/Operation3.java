package j03_연산자;

public class Operation3 {

	public static void main(String[] args) {
//		복합 대입 연산자
		int result = 0;
		System.out.println(result);
		
		result = result + 10;
		System.out.println(result);
		
		result += 20;
		System.out.println(result);
		
		result /= 2;
		System.out.println(result);
		
		result %= 3;
		System.out.println(result);

	}

}
